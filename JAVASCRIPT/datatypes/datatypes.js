// Number
let num = 42;
console.log("Number:");
console.log(num); // Output: 42

// String
let str = "Hello, world!";
console.log("\nString:");
console.log(str); // Output: Hello, world!

let string = 12 + 8 + "demo" + 5 + 5;
console.log(string);

// Boolean
let bool = true;
console.log("\nBoolean:");
console.log(bool); // Output: true

// Undefined
let undef;
console.log("\nUndefined:");
console.log(undef); // Output: undefined

// Null
let empty = null;
console.log("\nNull:");
console.log(empty); // Output: null

// Object
let obj = { name: "Alice", age: 25 };
console.log("\nObject:");
console.log(obj); // Output: { name: 'Alice', age: 25 }

// Array
let arr = [1, 2, 3, 4, 5];
console.log("\nArray:");
console.log(arr); // Output: [1, 2, 3, 4, 5]

// Function
let func = function() { return "Hello from a function!"; };
console.log("\nFunction:");
console.log(func); // Output: [Function: func]
console.log(func()); // Output: Hello from a function!

// BigInt
let bigInt = 1234567890123456789012345678901234567890n;
console.log("\nBigInt:");
console.log(bigInt); // Output: 1234567890123456789012345678901234567890n

// Date
let date = new Date();
console.log("\nDate:");
console.log(date); // Output: Current date and time

